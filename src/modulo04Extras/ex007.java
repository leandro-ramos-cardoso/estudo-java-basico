// 7. Crie um programa que calcule a média de três notas do tipo `double`.
package modulo04Extras;

public class ex007 {
    public static void main (String[] args) {
       double notaUm = 9.5;
       double notaDois = 7.3;
       double notaTres = 8.4;

       double media = (notaUm + notaDois + notaTres) / 3;

        System.out.println(media);
    }
}
